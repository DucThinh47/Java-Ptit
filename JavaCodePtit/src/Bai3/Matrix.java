import java.util.*;
public class Matrix {
    private int x,y;
    
    public Matrix(int x, int y){
        this.x = x;
        this.y = y;
    }
    
    public int getX(){
        return this.x;
    }
    
    public int getY(){
        return this.y;
    }
}
